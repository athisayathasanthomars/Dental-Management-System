    private void RefrushTable(){
    try{
         connection mysql=new connection();
         Connection con=mysql.connectDB();
         PreparedStatement ps1=con.prepareStatement("SELECT * FROM payment");
         ResultSet rs=ps1.executeQuery();
         DefaultTableModel model=(DefaultTableModel)payment_table.getModel();
         model.setRowCount(0);
         while(rs.next()){
           String payment_id =rs.getString("paymentid");
           String patient_id=rs.getString("patientid");
           String treatmentbook_id=rs.getString("treatmentbookid");
           String pay_description=rs.getString("description");
           String treatment_fees=rs.getString("treatmentfees");
           String room_fees=rs.getString("roomfees");
           String total=rs.getString("total");
           //create array for store data into patient_table
           String tbData[]={payment_id,patient_id,treatmentbook_id,pay_description,treatment_fees,room_fees,total};
           DefaultTableModel tb1Model=(DefaultTableModel)payment_table.getModel();
           //add string array data to table
           tb1Model.addRow(tbData);                   
         }
         
        }
        catch(Exception ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(null,"error happen.please try again..");
         autoID();
        }
    }
    private void sort(){
       DefaultTableModel model=(DefaultTableModel)payment_table.getModel();    
       TableRowSorter sorter=new TableRowSorter(model);
       payment_table.setRowSorter(sorter);
    }
    //Filter table with sorting object
    private void filter(String query){
       //sorting the table values
       DefaultTableModel model=(DefaultTableModel)payment_table.getModel();    
       TableRowSorter sorter=new TableRowSorter(model);
       payment_table.setRowSorter(sorter);
       sorter.setRowFilter(RowFilter.regexFilter(query));
       
    }
