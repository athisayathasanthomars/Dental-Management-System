    private void search_paymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_paymentActionPerformed
        // TODO add your handling code here:
        String payment_id=paymentid.getText();
        if(paymentid.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Enter ID to search..");    
        }
        else{
           try{
               connection mysql=new connection();
               Connection con=mysql.connectDB();
               PreparedStatement selectdata=con.prepareStatement("select * from payment where paymentid=?");
            selectdata.setString(1,payment_id);
            ResultSet rs=selectdata.executeQuery();
            if(rs.next()){
                paymentid.setText(rs.getString("paymentid"));
                String treatmentbook_id=rs.getString("treatmentbookid");
                treatmentbookid.setSelectedItem(treatmentbook_id);
                patientid.setText(rs.getString("patientid"));
                treatmentfees.setText(rs.getString("treatmentfees"));
                roomfees.setText(rs.getString("roomfees"));
                total.setText(rs.getString("total"));
                description.setText("description");
            }
            else{
                treatmentbookid.setSelectedItem(null);
                patientid.setText(null);
                treatmentfees.setText(null);
                roomfees.setText(null);
                total.setText(null);
                description.setText(null);
                JOptionPane.showMessageDialog(null,"no records are there!!");
                autoID();
            }
           }
           catch(Exception ex){
                JOptionPane.showMessageDialog(null,"Error..");
                autoID();
           }
        }
    }//GEN-LAST:event_search_paymentActionPerformed

    private void insert_paymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert_paymentActionPerformed
        if(paymentid.getText().equals("") || treatmentfees.getText().equals("") || total.getText().equals("")){
             JOptionPane.showMessageDialog(null,"All the fields are required.."); 
             RefrushTable();
        }
        else{
            try{
               connection mysql=new connection();
               Connection con=mysql.connectDB();
               String payment_id=paymentid.getText();
               String treatment_bookid=treatmentbookid.getSelectedItem().toString();
               String patient_id=patientid.getText();
               String treatment_fees=treatmentfees.getText();
               Integer treat_fees=Integer.valueOf(treatment_fees);
               String room_fees=roomfees.getText();
               Integer r_fees=Integer.valueOf(room_fees);
               String total_fees=total.getText();
               Integer t_fees=Integer.valueOf(total_fees);
               String pay_descrip=description.getText();
               PreparedStatement ps1=con.prepareStatement("INSERT INTO payment(paymentid,patientid,treatmentbookid,description,treatmentfees,roomfees,total) VALUES(?,?,?,?,?,?,?)");
               ps1.setString(1,payment_id);
               ps1.setString(2,patient_id);
               ps1.setString(3,treatment_bookid);
               ps1.setString(4,pay_descrip);
               ps1.setInt(5,treat_fees);
               ps1.setInt(6,r_fees);
               ps1.setInt(7,t_fees);
               ps1.executeUpdate();
               JOptionPane.showMessageDialog(null,"Inserted suncessfully..");
               autoID();
               RefrushTable();
               treatmentbookid.setSelectedItem(null);
               patientid.setText(null);
               treatmentfees.setText(null);
               roomfees.setText(null);
               total.setText(null);
               description.setText(null);
            }
            catch(Exception ex){
               JOptionPane.showMessageDialog(null,"Error");
               RefrushTable();
            }
        }
    }//GEN-LAST:event_insert_paymentActionPerformed

