    private void search_staffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_staffActionPerformed
        // TODO add your handling code here:
        String staff_id=staffid.getText();
        if(staffid.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter ID to search..");
        }
        else{
            try{
                connection mysql=new connection();
                Connection con=mysql.connectDB();

                PreparedStatement selectdata=con.prepareStatement("select * from staff where staffid=?");
            selectdata.setString(1,staff_id);
            ResultSet rs=selectdata.executeQuery();
            if(rs.next()){
                staffname.setText(rs.getString("staffname"));
                staffaddress.setText(rs.getString("staffaddress"));
                staffnic.setText(rs.getString("staffnic"));
                staffphonenumber.setText(rs.getString("contactno"));
                String staff_type=rs.getString("stafftype");
                    switch(staff_type) {
                        case "Manager":
                            stafftype.setSelectedItem("Manager");
                            break;
                        case "Nurse":
                            stafftype.setSelectedItem("Nurse");
                            break;
                        case "Receptionist":
                            stafftype.setSelectedItem("Receptionist");                
                            break;
                        default:
                            break;
                    }
                if(rs.getString("gender").equals("Male")){
                    gender_male.setSelected(true);
                    gender_female.setSelected(false);
                    
                }
                else if(rs.getString("gender").equals("Female")){
                    gender_female.setSelected(true);
                    gender_male.setSelected(false);
                    
                }

            }
            else{
                staffname.setText(null);
                staffaddress.setText(null);
                staffnic.setText(null);
                staffphonenumber.setText(null);
                stafftype.setSelectedItem(false);
                gender_male.setSelected(false);
                gender_female.setSelected(false);
                JOptionPane.showMessageDialog(null,"no records are there!!");
                autoID();
            }
            }
            catch(Exception ex){
                JOptionPane.showMessageDialog(null,"error happen.please try again..");
                autoID();
            }
        }
    }//GEN-LAST:event_search_staffActionPerformed

    private void insertstaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertstaffActionPerformed
        if(staffid.getText().equals("") || staffname.getText().equals("") || staffaddress.getText().equals("") || staffnic.getText().equals("") || staffphonenumber.getText().equals("")){
            JOptionPane.showMessageDialog(null,"All the fields are requried..");
        }
        else{
            try{
                connection mysql=new connection();
                Connection con=mysql.connectDB();
                String staff_id=staffid.getText();
                String staff_name=staffname.getText();
                String staff_address=staffaddress.getText();
                String staff_nic=staffnic.getText();
                String staff_phonenumber=staffphonenumber.getText();
                String staff_type=stafftype.getSelectedItem().toString();
                    PreparedStatement ps1=con.prepareStatement("INSERT INTO staff(staffid,staffname,staffaddress,staffnic,contactno,stafftype,gender) VALUES(?,?,?,?,?,?,?)");
                    ps1.setString(1,staff_id);
                    ps1.setString(2,staff_name);
                    ps1.setString(3,staff_address);
                    ps1.setString(4,staff_nic);
                    ps1.setString(5,staff_phonenumber);
                    ps1.setString(6,staff_type);
                    if(gender_male.isSelected()){
                        ps1.setString(7,gender_male.getText());
                        ps1.executeUpdate();
                    }
                    else{
                        ps1.setString(7,gender_female.getText());
                        ps1.executeUpdate();
                    }

                    JOptionPane.showMessageDialog(null,"Inserted suncessfully..");
                    autoID();
                    RefrushTable();
                    staffname.setText(null);
                    staffaddress.setText(null);
                    staffnic.setText(null);
                    staffphonenumber.setText(null);
                    stafftype.setSelectedItem(false);
                    gender_male.setSelected(false);
                    gender_female.setSelected(false);
                    
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null,"Please enter details..");
                autoID();
            }
        }
    }//GEN-LAST:event_insertstaffActionPerformed

    private void updatestaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatestaffActionPerformed
        // TODO add your handling code here:
        String staff_id=staffid.getText();
        if(staffid.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please enter id to update!!");
            autoID();
        }
        else{
            try{
                connection mysql=new connection();
                Connection con=mysql.connectDB();
                String staff_name=staffname.getText();
                String staff_address=staffaddress.getText();
                String staff_nic=staffnic.getText();
                String staff_phonenumber=staffphonenumber.getText();
                String staff_type=stafftype.getSelectedItem().toString();
                String staff_gender;
                if(gender_male.isSelected()){
                    staff_gender=gender_male.getText();
                }
                else{
                    staff_gender=gender_female.getText();
                }
                if(staffname.getText().equals("")){
                    JOptionPane.showMessageDialog(null,"Please enter id to update!!");
                    autoID();
                }
                else{
                    PreparedStatement ps=con.prepareStatement("UPDATE staff SET staffname=?,staffaddress=?,staffnic=?,contactno=?,stafftype=?,gender=? WHERE staffid=?");
                    ps.setString(1,staff_name);
                    ps.setString(2,staff_address);
                    ps.setString(3,staff_nic);
                    ps.setString(4, staff_phonenumber);
                    ps.setString(5,staff_type);
                    ps.setString(6,staff_gender);
                    ps.setString(7,staff_id);
                    ps.executeUpdate();
                    staffname.setText(null);
                    staffaddress.setText(null);
                    staffnic.setText(null);
                    staffphonenumber.setText(null);
                    stafftype.setSelectedItem(false);
                    gender_male.setSelected(false);
                    gender_female.setSelected(false);
                    JOptionPane.showMessageDialog(null,"updated suncessfully..");
                    autoID();
                    RefrushTable();
                }
            }
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null,"Please enter id and details to update!!");
                autoID();
            }
        }
    }//GEN-LAST:event_updatestaffActionPerformed

