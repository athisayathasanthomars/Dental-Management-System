    private void RefrushTable(){
    try{
         connection mysql=new connection();
         Connection con=mysql.connectDB();
         PreparedStatement ps1=con.prepareStatement("SELECT * FROM staff");
         ResultSet rs=ps1.executeQuery();
         DefaultTableModel model=(DefaultTableModel)staff_table.getModel();
         model.setRowCount(0);
         while(rs.next()){
           String staff_id=rs.getString("staffid");
           String staff_name=rs.getString("staffname");
           String staff_address=rs.getString("staffaddress");
           String staff_nic=rs.getString("staffnic");
           String contact_no=rs.getString("contactno");
           String staff_type=rs.getString("stafftype");
           String staff_gender=rs.getString("gender");
           //create array for store data into patient_table
           String tbData[]={staff_id,staff_name,staff_address,staff_nic,contact_no,staff_type,staff_gender};
           DefaultTableModel tb1Model=(DefaultTableModel)staff_table.getModel();
           //add string array data to table
           tb1Model.addRow(tbData);                   
         }
         
        }
        catch(Exception ex){
         JOptionPane.showMessageDialog(null,"error happen.please try again..");
         autoID();
        }
    }
