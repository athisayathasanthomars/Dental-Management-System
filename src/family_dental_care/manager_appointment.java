    private void search_appointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_appointmentActionPerformed
        // TODO add your handling code here:
        String appointment_id=appointmentid.getText();
        if(appointmentid.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Please enter ID to search..");
        }
        else{
           try{
            connection mysql=new connection();
            Connection con=mysql.connectDB();
            
            PreparedStatement selectdata=con.prepareStatement("select * from appointment where appointmentid=?");
            selectdata.setString(1,appointment_id);
            ResultSet rs=selectdata.executeQuery();
            if(rs.next()){
                appointmentid.setText(appointment_id);
                String doctor_id=rs.getString("doctorid");
                doctorid.setSelectedItem(doctor_id);
                String patient_id=rs.getString("patientid");
                patientid.setSelectedItem(patient_id);
                fees.setText(rs.getString("appointmentfees"));
                String app_date=rs.getString("date");
                date.setText(app_date);
                String app_time=rs.getString("time");
                time_show.setText(app_time);
                
            }
            else{
                autoID();
                patientid.setSelectedItem(null);
                fees.setText(null);
                time_show.setText(null);
                time.setToolTipText(null);
                doctorid.setSelectedItem(null);

                JOptionPane.showMessageDialog(null,"no records are there!!");
                
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"error happen.please try again..");
            autoID();
        }
        }
    }//GEN-LAST:event_search_appointmentActionPerformed

