    private void insert_appointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert_appointmentActionPerformed
        // TODO add your handling code here:
        String appointment_id=appointmentid.getText();
        if(appointment_id.equals("") || fees.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Please enter ID to search..");
           autoID();
        }
        else{
        try{
            connection mysql=new connection();
            Connection con=mysql.connectDB();

            String patient_id=patientid.getSelectedItem().toString();
            int app_fees=Integer.parseInt(fees.getText());
            String app_date=((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText();//covert date to string
            String app_time=time.getText();
            String doctor_id=doctorid.getSelectedItem().toString();
            PreparedStatement ps1=con.prepareStatement("INSERT INTO appointment(appointmentid,doctorid,patientid,date,time,appointmentfees,status) VALUES(?,?,?,?,?,?,?)");
            ps1.setString(1,appointment_id);
            ps1.setString(2,doctor_id);
            ps1.setString(3,patient_id);
            ps1.setString(4,app_date);
            ps1.setString(5,app_time);
            ps1.setInt(6,app_fees);
            ps1.setString(7,"pending");
            ps1.executeUpdate();
            JOptionPane.showMessageDialog(null,"Inserted suncessfully..");
            autoID();
            patientid.setSelectedItem(null);
            fees.setText(null);
            jDateChooser2.setCalendar(null);
            //time.setValue(null);
            date.setText(null);
            time_show.setText(null);
            doctorid.setSelectedItem(null);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,"Please enter details..");
            autoID();
        }
        }
    }//GEN-LAST:event_insert_appointmentActionPerformed

