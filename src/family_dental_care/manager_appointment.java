    private void RefrushTable(){
    try{
         connection mysql=new connection();
         Connection con=mysql.connectDB();
         PreparedStatement ps1=con.prepareStatement("SELECT * FROM appointment");
         ResultSet rs=ps1.executeQuery();
         DefaultTableModel model=(DefaultTableModel)appointment_table.getModel();
         model.setRowCount(0);
         while(rs.next()){
           String appointment_id =rs.getString("appointmentid");
           String doctor_id=rs.getString("doctorid");
           String patient_id=rs.getString("patientid");
           String app_date=rs.getString("date");
           String app_time=rs.getString("time");
           String app_fees=rs.getString("appointmentfees");
           String app_status=rs.getString("status");
           //create array for store data into patient_table
           String tbData[]={appointment_id,doctor_id,patient_id,app_date,app_time,app_fees,app_status};
           DefaultTableModel tb1Model=(DefaultTableModel)appointment_table.getModel();
           //add string array data to table
           tb1Model.addRow(tbData);                   
         }
         
        }
        catch(Exception ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(null,"error happen.please try again..");
         autoID();
        }
    }
    private void sort(){
       DefaultTableModel model=(DefaultTableModel)appointment_table.getModel();    
       TableRowSorter sorter=new TableRowSorter(model);
       appointment_table.setRowSorter(sorter);
    }
    //Filter table with sorting object
    private void filter(String query){
       //sorting the table values
       DefaultTableModel model=(DefaultTableModel)appointment_table.getModel();    
       TableRowSorter sorter=new TableRowSorter(model);
       appointment_table.setRowSorter(sorter);
       sorter.setRowFilter(RowFilter.regexFilter(query));
       
    }
