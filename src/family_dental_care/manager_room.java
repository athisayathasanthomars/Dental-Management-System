    private void search_roomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_roomActionPerformed
        // TODO add your handling code here:
        String room_id=roomid.getText();
        if(roomid.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Please enter ID to search..");
        }
        else{
           try{
            connection mysql=new connection();
            Connection con=mysql.connectDB();
            
            PreparedStatement selectdata=con.prepareStatement("select * from room where roomid=?");
            selectdata.setString(1,room_id);
            ResultSet rs=selectdata.executeQuery();
            if(rs.next()){
                roomid.setText(rs.getString("roomid"));
                String staff_id=rs.getString("staffid");
                staffid.setSelectedItem(staff_id);
                String room_type=rs.getString("roomtype");
                roomtype.setSelectedItem(room_type);
                RefrushTable();
                
            }
            else{
                roomtype.setSelectedItem(null);
                staffid.setSelectedItem(null);
                JOptionPane.showMessageDialog(null,"no records are there!!");
                autoID();
                RefrushTable();
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"error happen.please try again..");
            autoID();
        }
        }
        
    }//GEN-LAST:event_search_roomActionPerformed

